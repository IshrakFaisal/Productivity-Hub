export function getToolById(id: string) {
  return productivityTools.find((item) => item.id === id);
}

export function getToolsByNames(names: string[]) {
  return names.map((name) => productivityTools.find((item) => item.name === name)).filter(Boolean) as ProductivityTool[];
}

export function getAlternatives(tool: ProductivityTool) {
  const named = getToolsByNames(tool.alternatives);
  if (named.length >= 3) return named;
  return productivityTools
    .filter((candidate) => candidate.id !== tool.id && candidate.categories.some((category) => tool.categories.includes(category)))
    .sort((a, b) => b.mainAppScore - a.mainAppScore)
    .slice(0, 4);
}

export function getMainAppVerdict(tool: ProductivityTool) {
  if (tool.mainAppScore >= 8) {
    return `${tool.name} can be your productivity home base if your work matches its structure. You may still want a focused task or calendar layer where its weaker scores show up.`;
  }
  if (tool.mainAppScore >= 6) {
    return `${tool.name} is better as a strong specialist than as the entire operating system. Use it for its sharpest job, then pair it deliberately.`;
  }
  return `${tool.name} should usually be a supporting app, not the center of the stack. It shines when it has a clear lane and does not need to hold every workflow.`;
}
